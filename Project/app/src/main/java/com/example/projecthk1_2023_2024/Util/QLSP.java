package com.example.projecthk1_2023_2024.Util;

public class QLSP {
    String tenSP;
    String maLo;
    String slTon;
    String ttXuat;
    String hsd;

    public QLSP(String tenSP, String maLo, String slTon, String ttXuat, String hsd) {
        this.tenSP = tenSP;
        this.maLo = maLo;
        this.slTon = slTon;
        this.ttXuat = ttXuat;
        this.hsd = hsd;
    }

    public String getTenSP() {
        return tenSP;
    }

    public void setTenSP(String tenSP) {
        this.tenSP = tenSP;
    }

    public String getMaLo() {
        return maLo;
    }

    public void setMaLo(String maLo) {
        this.maLo = maLo;
    }

    public String getSlTon() {
        return slTon;
    }

    public void setSlTon(String slTon) {
        this.slTon = slTon;
    }

    public String getTtXuat() {
        return ttXuat;
    }

    public void setTtXuat(String ttXuat) {
        this.ttXuat = ttXuat;
    }

    public String getHsd() {
        return hsd;
    }

    public void setHsd(String hsd) {
        this.hsd = hsd;
    }
}
